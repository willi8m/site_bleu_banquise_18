function retourback() {
    var x = document.getElementsByClassName("back");
    var i;
    for (i = 0; i < x.length; i++) {
        x[i].addEventListener('click', function() {
         
        //Changement de background, retour a l'equipe complete
            document.getElementById("imgteam").style.background = "url('../img/equipe/Team01.jpg') no-repeat center";
            document.getElementById("imgteam").style.backgroundSize = "cover";
            document.getElementById("imgteam").style.animation = "animebg 1s 1";
            $("#imgteam").bind('webkitAnimationEnd', function(){this.style.webkitAnimationName = '';});
            document.getElementById("imgteam").style.animationFillMode = "forwards";  
            
        //retournement de la carte   
            $(this).parent().toggleClass('flip');

        }); 
    }
};



